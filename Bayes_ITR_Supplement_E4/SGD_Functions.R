
q_truth <- function(a, x, max_to_min){
  
  # Calculate the simulated true Q-value.
  # Input: 1. a (scalar): the continuous treatment a;
  #        2. x (scalar): the covariate x;
  #        3. max_to_min (boolean): if max_to_min=TRUE, convert the maximization problem to a minimization problem by negating the Q-value.
  # Output: y (scalar): the simulated true Q-value.
  
  y <- cos(a) + sin(x*a)
  if (max_to_min){ return(-y) # from max to min
  }else { return(y) }
}

q_function_conservative_mean <- function(a, x, phi){
  
  # Calculate the (alternative) conservative Q-value.
  # Input: 1. a (scalar): the continuous treatment a;
  #        2. x (scalar): the covariate x;
  #        3. phi (matrix, dim=c(num_samples, B*3+1)): the posterior samples of (mu_Y(dim=1), gamma(dim=B), beta_X^Y(dim=B), delta_{A*X}(dim=B)), where num_samples denotes the number of posterior samples, and B=B0-1 denotes the degrees of freedom of the cubic B-spline expansion after imposing the sum-to-zero constraint.
  # Output: y_min (scalar): the (alternative) conservative Q-value.
  
  # transfrom data to spline basis
  a_bs <- predict(a_spline, a) %*% a_trans
  x_bs <- predict(x_spline, x) %*% x_trans
  xa_bs <- predict(xa_spline, x*a) %*% xa_trans
  # calculate q-value
  y <- cbind(rep(1,length(x)),a_bs,x_bs,xa_bs) %*% t(phi)
  y_mean <- colMeans(y,dims=1); min_index <- which.min(y_mean)
  y_min <- y[,min_index] # lower bound
  # check whether out of the boundary
  out_index <- unique(c(which(a<a_lower|a>a_upper), 
                        which((x*a)<xa_lower|(x*a)>xa_upper)))
  y_min[out_index] <- 0
  return(y_min)
}

